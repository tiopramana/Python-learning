import math

# Functions matematika dasar

# math.sqrt(x): Menghitung akar kuadrat dari x
print(math.sqrt(16)) 

# math.pow(x, y): Menghitung x pangkat y
print(math.pow(2, 10))

# math.ceil(x): Membulatkan ke atas (ke bilangan bulat terdekat).
print(math.ceil(3.2))

# math.floor(x): Membulatkan ke bawah (ke bilangan bulat terdekat).
print(math.floor(3.2))

#================================================================

# Functions Matematika Trigonometric

# math.sin(x): Menghitung sinus dari x (dalam radian).
print(math.sin(math.pi / 2))

# math.cos(x): Menghitung kosinus dari x (dalam radian)
print(math.cos(0))

# math.degrees(x): Mengubah sudut dari radian ke derajat
print(math.degrees(math.pi))

# math.radians(x): Mengubah sudut dari derajat ke radian.
print(math.radians(90))

# =================================================================

# Functions Logarithm

# math.log(x, base): Menghitung logaritma dari x dengan basis tertentu (default adalah basis e).
print(math.log(10, 5))

# math.log10(x) : Logarithm berbasis 10
print(math.log10(1000))

#============================================================================

# Konstanta Matematika

# math.pi: Nilai π (pi), 3.141592653589793.
print(math.pi)

# math.e: Nilai bilangan Euler (e), 2.718281828459045.
print(math.e)
